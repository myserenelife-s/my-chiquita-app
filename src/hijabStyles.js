// Get today's style based on day of month
export const getTodaysStyle = () => {
    const dayOfMonth = new Date().getDate();
    const index = (dayOfMonth - 1) % HIJAB_STYLES.length;
    return HIJAB_STYLES[index];
};

// Filter styles by category
export const getStylesByCategory = (category) => {
    if (category === 'all') return HIJAB_STYLES;
    return HIJAB_STYLES.filter(style => style.category === category);
};

// Filter styles by difficulty
export const getStylesByDifficulty = (difficulty) => {
    return HIJAB_STYLES.filter(style => style.difficulty === difficulty);
};
